    combineNumbers(extracted) {
        let obj = {};
        let spelling = '';
        let ipa = '';
        let th = '';
        extracted.forEach((val, index) => {
            if (val.spelling) spelling = val.spelling + `${index == 0 ? '' : '-'}` + spelling;
            if (val.ipa) ipa = val.ipa + `${index == 0 ? '' : '-'}` + ipa;
            if (val.th) th = val.th + `${index == 0 ? '' : '-'}` + th;
        });
        obj.spelling = spelling;
        obj.ipa = ipa;
        obj.th = th;
        return obj;
    },
